# Integers operators
a = 11
b = 2
c = a + b
d = a - b
e = a * b
f = a / b
# floor division
g = a // b
# modulo
h = a % b
i = a**b

print(c, d, e, f, g, h, i)

# positive and negative integers floor division & modulo is not equal
# int class
# base between 2 ~ 36
